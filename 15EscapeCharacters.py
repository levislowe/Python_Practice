# Escape Character

txt = "We are the so-called \"Vikings\" from the north." # Allows the use of the "" in the txt variable
print(txt)

txt = "We are the so-called \'Vikings\' from the north." # Allows the use of the '' in the txt variable
print(txt)

txt = "We are the so-called \\Vikings\\ from the north." # Allows the use of the \\ in the txt variable
print(txt)

txt = "We are the so-called \nVikings from the north." # \n make a new line in the txt variable
print(txt)

txt = "We are the so-called \rVikings from the north." # \r uses a carriage return in the txt variable
print(txt)

txt = "We are the so-called \tVikings from the north." # \t puts a tab in the txt variable
print(txt)

txt = "We are the so-called \bVikings from the north." # \b puts a backspace in the txt variable
print(txt)

txt = "We are the so-called \fVikings from the north." # \f puts form feeding in the txt variable
print(txt)
