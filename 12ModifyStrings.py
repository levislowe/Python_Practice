#Upper Case

a = "Hello World "
print(a.upper()) # Displays HELLO WORLD

# Lower Case

print(a.lower()) # Displays hello world

# Remove Whitespace

print(a.strip()) # Removes the space after d since nothing comes after it

# Replace String

print(a.replace("H", "J")) # Replaces H with J

# Split String

print(a.split(" ")) # returns ['Hello', 'World', '']